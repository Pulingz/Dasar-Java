public class perbandingan {
    public static void main(String[] args) {
        int a = 100;
        int b = 50;
        int c = 100;

        System.out.println(a > b | a < c);
        System.out.println(a < b | a > c);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
